const pad = (n) => n < 10 ? `0${n}` : n;

export const generateAllMarkedDates = (year = new Date().getFullYear()) => {
    const marked = {};

    CALENDARIO_DATA.forEach(month => {
        const monthNum = pad(month.monthIndex + 1);

        month.events.forEach(event => {
            const dayNum = pad(event.day);
            const dateString = `${year}-${monthNum}-${dayNum}`;
            const color = STATUS_COLORS[event.status] || STATUS_COLORS.normal;

            // Basic dot marking
            marked[dateString] = {
                marked: true,
                dotColor: color,
                // Add metadata for custom render if needed
                customStyles: {
                    container: {
                        backgroundColor: event.status === 'recesso' ? '#fee2e2' : undefined,
                        borderRadius: 8
                    },
                    text: {
                        color: event.status === 'recesso' ? '#ef4444' : undefined,
                        fontWeight: 'bold'
                    }
                }
            };
        });
    });

    return marked;
};

export const getEventsForDate = (dateString) => {
    if (!dateString) return [];

    // dateString format YYYY-MM-DD
    const parts = dateString.split('-');
    const day = parseInt(parts[2], 10);
    const monthIndex = parseInt(parts[1], 10) - 1;

    const monthData = CALENDARIO_DATA.find(m => m.monthIndex === monthIndex);
    if (!monthData) return [];

    return monthData.events.filter(e => e.day === day);
};
